

angka_pertama = float(input("masukan bilngan pertama : "))
operator = (input("masukan operator : "))
angka_kedua = float(input("masukab bilangan kedua : "))


if operator == "/":
    hasil = angka_pertama / angka_kedua
    print ("hasil nya adalah = ", hasil)

elif operator == "+" :
    hasil = angka_pertama + angka_kedua
    print ("hasil nya adalah : ", hasil)

elif operator == "*" :
    hasil = angka_pertama * angka_kedua
    print ("hasil nya adalah =", hasil)

elif operator == "-" :
    hasil = angka_pertama - angka_kedua
    print ("hasil nya adalah = ", hasil)



